package info.adamjsmith.squarebomber.objects;

public class StaticObject extends GameObject {
	public float width;
	public float height;
	public float x;
	public float y;

	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
	public float getWidth() {
		return width;
	}
	
	public float getHeight() {
		return height;
	}
}
