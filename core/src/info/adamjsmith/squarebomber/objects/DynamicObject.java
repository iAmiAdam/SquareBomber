package info.adamjsmith.squarebomber.objects;

public class DynamicObject extends GameObject {
	public float width;
	public float height;
	public float x;
	public float y;
		
	public float getHeight() {
		return height;
	}
	
	public float getWidth() {
		return width;
	}
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
}
